public class TwoNumbers {
    public static void main(String[] args) {
        int numbOne = 22;
        double numbTwo = 13;
        int numbTwoTwo = 13;

        System.out.println(numbTwo + numbOne);
        System.out.println(numbOne - numbTwo);
        System.out.println(numbOne * numbTwo);
        System.out.println(numbOne / numbTwo);
        System.out.println(numbOne / numbTwoTwo);
        System.out.println(numbOne % numbTwo);

    }
}
